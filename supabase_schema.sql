-- ==========================================================================
-- 8. Create Committee Table
-- ==========================================================================
create table committee (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  place text not null,
  position text not null,
  image_url text not null
);

-- Enable Row Level Security (RLS)
alter table committee enable row level security;

-- Policies for Committee
create policy "Allow public read access to committee" on committee for select using (true);
create policy "Allow public insert access to committee" on committee for insert with check (true);
create policy "Allow public update access to committee" on committee for update using (true);
create policy "Allow public delete access to committee" on committee for delete using (true);


